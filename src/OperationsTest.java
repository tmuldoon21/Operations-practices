
public class OperationsTest 
{

	public static void main(String[] args) 
	{
		int number; //Declaration statement 
		number = 20; //assignment statement
		number +=5; //number = number  + 5
		System.out.print(number);
		number /=2;
		System.out.println(number);
		number-=6;
		System.out.println(number);
		number*=4;
		System.out.println(number);
		number%=2;
		System.out.println(number);
	}

}
